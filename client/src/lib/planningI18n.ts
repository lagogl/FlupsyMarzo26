const MONTHS_IT_FULL = ["Gennaio","Febbraio","Marzo","Aprile","Maggio","Giugno","Luglio","Agosto","Settembre","Ottobre","Novembre","Dicembre"];
const MONTHS_EN_FULL = ["January","February","March","April","May","June","July","August","September","October","November","December"];
const MONTHS_IT_SHORT = ["Gen","Feb","Mar","Apr","Mag","Giu","Lug","Ago","Set","Ott","Nov","Dic"];
const MONTHS_EN_SHORT = ["Jan","Feb","Mar","Apr","May","Jun","Jul","Aug","Sep","Oct","Nov","Dec"];

export const MONTHS_IT = MONTHS_IT_FULL;
export const MONTHS_EN = MONTHS_EN_FULL;

export function translateMonthLabel(label: string, lang: Lang): string {
  if (lang === "it") return label;
  for (let i = 0; i < 12; i++) {
    if (label.includes(MONTHS_IT_FULL[i])) return label.replace(MONTHS_IT_FULL[i], MONTHS_EN_FULL[i]);
    if (label.includes(MONTHS_IT_SHORT[i])) return label.replace(MONTHS_IT_SHORT[i], MONTHS_EN_SHORT[i]);
  }
  return label;
}

